// Atibuição de valores às variáveis
// adição
let a = 1;
let b = 5;
// a + b
console.log('Resultado de a + b:', a + b);

// subtração
let x = 50;
let y = 15;
// 50 - 15
console.log('Resultado de x - y:', x - y);

// multiplicação
x = 4;
y = 12;
// 4 * 12
console.log('Resultado de x * y:', x * y);

/* Continuar desse ponto */
// divisão
x = 48;
y = 4;
// 48 / 4
console.log('Resultado de x / y:', x / y);

/* Ordem dos cálculo em JS */
// 1 + 100 * 5
let m = 1;
let n = 100;
let o = 5;
console.log('Resultado de 1 + 100 * 5:', m + n * o);
// agupando por operador
// (1 + 100) * 5
console.log('Resultado de (1 + 100) * 5:', (m + n) * o);

// Operador de concatenação (+)
let escola = "Senac"
let estado = "Piauí"
let separador = " ";
console.log(escola + separador + estado);

// Operador de módulo (%)
x = 12;
y = 5;
// 12 % 5
console.log('Resultado de x % y:', x % y);

