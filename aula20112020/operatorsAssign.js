// Operadores de atribuição
// atribuição (+)
let n1 = 4;
let n2 = 2;

// atribuição de adição (n1 += n2)
console.log(n1 += n2);

// atribuição de subtração (n1 -= n2)
console.log(n1 -= n2);

// atribuição de subtração (n1 *= n2)
console.log(n1 *= n2)

// atribuição de subtração (n1 /= n2)
console.log(n2 /= n1)
